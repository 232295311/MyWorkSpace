/**
 * @param {number} n - a positive integer
 * @return {number}
 */
var hammingWeight = function (n) {
  //输入的n是十进制的 比如1011，n输入11.
  let count = 0;
  while (n > 0) {
    count = count + (n & 1);
    n = n >>> 1; //不能用n>>1 因为>>代表有符号位运算，当n大于2^31的时候容易出错
  }
  return count;
};
// 2**31 =>2147483648

// 2147483649>>1 => -1073741824 如果大于2^31 不正常
// 2147483649>>>1 => 1073741824
// 2147483647>>1 => 1073741823 如果小于2^31 正常
// 2147483647>>>1 => 1073741823
